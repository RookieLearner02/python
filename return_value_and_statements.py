
def add_numbers(number1, number2):
    """
    This function helps us to add the 2 numbers.

    Args:
        number1 (number): accepts the number, this is your first number which you want to add
        number2 (number): accepts the number, this is your second number which you want to add

    Returns:
        number: This will return the sum of 2 numbers
    """
    sum = number1 + number2
    return sum
    # return must be the last statement in the function.

print(add_numbers(96.25, 98))
